export function getPreviewParagraphCount(content: string, previewContent: string): number {
  const allParagraphs = content.split('\n\n').filter((p) => p.trim());
  const previewParagraphs = previewContent.split('\n\n').filter((p) => p.trim());
  return Math.min(previewParagraphs.length, allParagraphs.length);
}
